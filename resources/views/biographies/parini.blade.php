                        <h2>L'Opera</h2>
                        <div class="article-preview p-3">
                            <h3>Le Odi 1758-1766</h3>
                            <p class="p-3">Le Odi composte tra il 1758 e il 1766 mediano tra la componente
                                classicistica formale e le novità tematiche dell’illuminismo.

                                Nella Vita rustica si celebra l’utilità sociale del mondo contadino, motivo
                                significativo anche nel Giorno (“Il risveglio del giovin signore”); nella Salubrità
                                dell’aria si polemizza contro l’amministrazione pubblica milanese, attraverso il ricorso
                                ad immagini tratte dalla scienza medica, come anche nell’Innesto del vaiuolo,
                                componimento pienamente inserito nel dibattito scientifico contemporaneo.

                                Nell’ode L’educazione il poeta si esprime circa la formazione, attraverso la proposta di
                                una pedagogia fondata sulla conciliazione tra i valori cristiani e illuministi. Nell’ode
                                La musica svolge una azione di denuncia civile per mezzo della polemica contro la
                                pratica dell’evirazione dei bambini futuri cantanti . Con Il bisogno , si pone sul solco
                                del trattato di Beccaria, Dei delitti e delle pene, rifiutando le punizioni eccessive e
                                sottolineando la necessità di prevenire i crimini.

                                Nelle odi successive alcuni componenti si segnalano per l’impronta autobiografica, come
                                La caduta. Con le odi Il messaggio, del 1793, e Alla musa, del 1795, il poeta lascia il
                                suo testamento spirituale celebrando il valore imperituro della bellezza,della poesia e
                                della integrità morale.
                            </p>
                        </div>
                        <h3>Il Giorno Il Mattino1763</h3>
                        <div class="article-preview p-3">
                            <p class="p-3">Molto tormentata, frutto di continue riprese e correzioni, destinata ad
                                una insuperata frammentarietà, la scrittura del Giorno nasce con il progetto e la
                                stesura, annunciati nella dedica “Alla moda” (una breve prosa che pone la prima parte de
                                Il mattino , sotto l’egida della mutevole dea della moda, divinità venerata dalla classe
                                sociale aristocratica. In suo nome viene rifiutata la logica del buon senso, quella
                                ragionevolezza morale che per l’appunto la nobiltà ha smarrito), dei poemetti Il Mattino
                                (1763) e Il Mezzogiorno (1765). Il Mattino è dedicato alla minuziosa descrizione delle
                                attività del “giovin signore”, su cui è feroce l’ironia dell’autore, che aveva fatto
                                diretta esperienza, presso la famiglia Serbelloni, dei comportamenti frivoli della
                                nobiltà. Il poemetto vedrà stesure successive, nelle quali verrà espunta, come risulta
                                dagli autografi, la dedica iniziale.

                                Nel Mezzogiorno la rappresentazione si arricchisce di nuovi personaggi attraverso
                                conversazioni ,di argomento vario, delle quali il poeta mostra la futilità .Attraverso
                                episodi celebri ,come quello della “ Vergine cuccia” , Parini mette in luce la volgare
                                aridità morale della classe nobiliare e la sua cinica leggerezza. Nelle edizioni
                                successive dei due poemetti riduce il vigore polemico , pur senza distogliere lo sguardo
                                dal malcostume nobiliare.

                                Il progetto della Sera, annunciato nella dedica “ Alla moda”, non viene realizzato. Ad
                                esso si sostituiscono le ultime due parti dell’opera , Il Vespro, in cui confluisce la
                                parte finale del Mezzogiorno del 1765, e La notte. In queste due ultime parti il tono
                                della meditazione si fa più cupo, emergono le caricature , come nel catalogo degli
                                “imbecilli”, i colori si fanno scuri e nelle ombre della notte, l’ironia lascia spazio
                                all’amarezza del sarcasmo, di cui il ritratto della folla grottesca offre emblematica
                                esemplificazione. Manca nell’ultima sezione la fiducia ottimistica nella possibilità di
                                una collaborazione riformistica con il potere e viene meno la tensione agonistica
                                dell’impegno civile che lascia spazio a una riflessione più pessimista sulle possibili
                                trasformazioni della società, in coincidenza con la crisi profonda della rivoluzione
                                francese e il crollo delle speranze in un rinnovamento pacifico. Del resto , come è
                                stato scritto ( L. Caretti Introduzione a Il Giorno, Poesie e prose varie), la
                                “complessa gestazione del Giorno , protrattasi per anni e anni e rimasta senza una
                                risoluzione definitiva ,attesta prima di tutto il progressivo affievolirsi nel Parini
                                dell’iniziale stimolo polemico e della giovanile accensione moralistica che sono
                                all’origine dell’idea stessa del Giorno. Si è trattato del venir meno, via via, di
                                quella illuministica fiducia nel cordiale e fecondo rapporto tra intellettuali e società
                                che fu propria non solo del Parini, ma anche di tutti gli uomini di cultura lombardi che
                                facevano capo ai Trasformati o al gruppo del Caffè”.
                            </p>
