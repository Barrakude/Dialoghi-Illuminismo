                        <h2>I rapporti con la cultura illuministica milanese
                        </h2>
                        <div class="article-preview p-3">
                            <p class="p-3">Dopo il licenziamento dalla famiglia Serbelloni, diviene precettore di
                                Carlo Imbonati. Nel 1763 pubblica Il Mattino e nel 1765 Il Mezzogiorno, ovvero le prime
                                due parti del poemetto Il Giorno. Con queste opere e con le prime Odi di argomento
                                civile Parini si consacra come uno degli esponenti più vitali della cultura milanese
                                partecipando in prima persona alla elaborazione delle istanze illuministiche italiane.
                                Notevoli i rapporti con il gruppo del Caffè, anche se spesso improntati ad una certa
                                freddezza, date le critiche subite da Parini per il suo accademismo letterario e per la
                                sua distanza dalle posizioni più radicali degli illuministi lombardi. Del gruppo legato
                                al Caffè Parini, infatti, non approvava il cosmopolitismo filosofico e propendeva per un
                                maggior rispetto della purezza culturale italiana, nel segno di un classicismo
                                tradizionale, apertamente rifiutato, invece, dai Verri, appassionati di cultura francese
                                e rivolti ad una letteratura finalizzata all’utile e al dibattito politico.
                            </p>
                            <h2>Gli anni dell’insegnamento pubblico e i rapporti con il potere</h2>
                            <div class="article-preview p-3">
                                <p class="p-3">La pubblicazione dei poemetti e il ruolo culturale rivestito
                                    nell’ambiente milanese determinano

                                    la prestigiosa nomina alla cattedra di Belle Lettere presso le Scuole Palatine di
                                    Milano. Seguono altri importanti incarichi quali la partecipazione a commissioni
                                    governative per la riforma dei testi scolastici e per il riordinamento delle scuole.
                                    In questi anni, tra il 1766 e il 1785, scrive alcune Odi e continua a correggere e
                                    rielaborare Il Giorno cui aggiunge, senza portarli a definitivo compimento,due nuovi
                                    poemetti Il Vespro e La notte, pubblicati dopo la sua morte.

                                    Negli anni successivi, caratterizzati dalle riforme autoritarie di Giuseppe II,
                                    succeduto a Maria Teresa, mantiene gli incarichi governativi, tra molte incertezze,
                                    mentre altri intellettuali vengono allontanati : si tratta di un momento difficile
                                    per gli uomini di cultura illuministi che vedono sgretolarsi il sogno di una
                                    collaborazione riformistica con il governo austriaco. L’Ode La tempesta si fa
                                    portavoce del difficile momento storico.</p>
