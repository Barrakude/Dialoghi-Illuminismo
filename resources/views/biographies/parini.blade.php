                                    Pochi anni più tardi, sulla sua figura solitaria e fiera gli scrittori, tra cui il
                                    Foscolo, costruiranno il mito romantico–risorgimentale dell’intellettuale eroico,
                                    impegnato nella rinascita etica e politica dell’Italia.</p>
