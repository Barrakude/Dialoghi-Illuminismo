                            <h2>La nascita e la formazione</h2>
                            <div class="article-preview p-3">
                                <p class="p-3">Giuseppe Parini nasce nel 1729 in Brianza, a Bosisio, da una famiglia
                                    di modeste condizioni. Più tardi si trasferisce a Milano, presso una prozia, per
                                    proseguire gli studi presso la scuola dei Barnabiti, dove è ordinato sacerdote
                                    (1754) senza una particolare vocazione, bensì per necessità economiche. La zia,
                                    mancata nel1741, gli aveva infatti garantito una piccola rendita purchè abbracciasse
                                    lo stato sacerdotale.
                                    Nel 1752, portata a termine la prima fase della sua formazione culturale, pubblica
                                    la prima raccolta di versi,Alcune poesie di Ripano Eupilino, grazie alla quale è
                                    ammesso all’Accademia dei Trasformati, centro di rielaborazione culturale delle
                                    istanze illuministiche, secondo un orientamento moderato, pienamente rispondente
                                    alla visione ideologica del giovane Parini attento ad una conciliazione tra gli
                                    elementi della tradizione e l’impegno civile.

                                    Le conoscenze ottenute presso l’Accademia, le discussioni e le letture di versi ivi
                                    svolte gli valgono l’ingresso, in qualità di precettore, nella famiglia dei duchi
                                    Serbelloni, esperienza importantissima perché gli garantisce un punto di
                                    osservazione privilegiato sul mondo nobiliare e perché corrisponde ad un vero e
                                    proprio itinerario di formazione. Infatti, nell’Accademia e nella famiglia signorile
                                    presso cui è al servizio conosce la realtà dell’aristocrazia e partecipa ad un
                                    ambiente culturalmente fervido.

                                    In questi anni, tra il 1754 e il 1762, scrive le prime Odi, realizza,nel 1757, il
                                    Dialogo sopra la nobiltà e inizia la composizione dei versi destinati a confluire
                                    nella sua opera più importante, Il Giorno.</p>
                            </div>
