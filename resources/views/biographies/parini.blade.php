                                    Verri ai lavori di una commissione che si occupava di teatro, di scuola e finanza
                                    pubblica; si realizza un profondo riavvicinamento tra i due intellettuali, infatti
                                    il Verri modera l'ostilità giovanile e ammira la rettitudine morale dell'anziano
