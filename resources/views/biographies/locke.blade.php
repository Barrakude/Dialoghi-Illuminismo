                        <h3>Saggio sull'intelletto umano 1688</h3>
                        <div class="article-preview p-3">
                            <p class="p-3">E' il trattato che porta Locke ad occupare, nella storia dell'empirismo
                                inglese, un posto analogo a quello che occupa Cartesio nella storia del moderno
                                razionalismo. L'opera si articola in quattro libri: Non esistono principi né idee
                                innate; Le idee; Le parole; La conoscenza e la probabilità. Locke affronta
                                preliminarmente il problema della ricerca delle origini e dei limiti della conoscenza,
                                assumendo l'esperienza come fondamento del conoscere. Sul problema delle origini della
                                conoscenza, Locke critica la posizione di Cartesio, affermando che non esistono idee
                                innate, ma solo idee derivate. Poiché l'unica fonte di conoscenza è data dalla
                                sensazione, le idee derivano dall'esperienza dei sensi.
                            </p>
                        </div>
