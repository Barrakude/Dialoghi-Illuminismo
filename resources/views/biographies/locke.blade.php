                            <h3>L''Epistula de Tolerantia" 1689</h3>
                            <p class="p-3">
                                Esule in Olanda, fin dal 1683, Locke formula le sue concezioni in materia di tolleranza
                                religiosa, confermate anche dagli accorati racconti degli orrori patiti che venivano
                                facendo gli ugonotti francesi rifugiati in Olanda. Nel 1685 egli scrive in forma di
                                lettera e in latino (Lettera sulla tolleranza), il suo celebre trattato, pubblicato
                                anonimo quattro anni dopo (1689). E' un documento di aperta condanna della politica del
                                re e della chiesa d'Inghilterra, dove lasciando trapelare un'amara ironia, Locke trova
                                strano che gli uomini debbano essere costretti col ferro e col fuoco a professare certe
                                credenze, e che gli zelatori della religione si adoperino perché altri uomini, dei quali
                                ardentemente essi desiderano la salvezza, spirino tra i tormenti qualora non vogliano
                                convertirsi. La tolleranza di coloro che dissentono dagli altri in fatto di religione, è
