                            </p>
                            </br>
                            <p class="p-3">
                                In letteratura, l'interesse per l'oriente si sviluppa nel Seicento e culmina nel
                                Settecento sotto l'impulso dei viaggi di esplorazione, delle ricerche etnografiche, dei
                                viaggi dei missionari, dando avvio ad un'ampia letteratura in cui la ricchezza del mito
                                orientale compare in tutte le sue sfaccettature. Spesso evocato come una dimensione
                                atemporale, che fa da sfondo alle vicende romanzesche, l'Oriente è anche e soprattutto
                                metafora di una civiltà o di un popolo, cornice di interesse etnografico, o semplice
                                rêverie che dà sfogo al gusto dell'esotismo, alla fascinazione per costumi pagani e
                                raffinati. I suoi molteplici significati sono in relazione con le diverse funzioni
                                narrative. In quanto dimensione senza storia, l'Oriente permette di dare rilievo a
                                imperituri valori morali individuali e collettivi. In quanto luogo elettivo di una
                                diversa religione, l'Islam, esso ha una funzione sia narrativa sia discorsiva. Così ne
                                Le Lettere persiane, di Montesquieu, l'ambiente orientale è pretesto per discutere in
                                realtà di politica e di filosofia morale. Il serraglio e il fosco dramma che ne deriva
                                sono elementi portanti di un preciso discorso ideologico. A lungo confrontato con il
                                cristianesimo, di cui offre un'immagine speculare delle superstizioni, del fanatismo,
                                dell'intolleranza, l'Islam di Montesquieu è astrazione che ha valore di provocatorio
                                strumento di discussione: l'autore sembra infatti chiedersi se, lungi dal costituire
                                un'alternativa razionale alla religione occidentale, la stasi e l'immobilismo del
                                “dispotismo orientale” non rendano quelle istituzioni più radicalmente irreversibili di
                                quelle occidentali. In questo romanzo infatti la componente politica e quella del
                                rapporto tra i sessi sono intimamente connesse tra loro dalla teoria del dispotismo –un
                                termine quest'ultimo diffuso e reso popolare proprio da Montesquieu. La tirannia del
                                serraglio che assoggetta molte mogli al capriccio di un solo marito, è la metafora
                                quotidiana del sultanato, che concentra potere e ricchezze nelle mani di un sovrano
                                assoluto, riducendo la società ad esso “assoggettata” ad una plaga di sudditanza
                                passiva. Fiaccata e prostrata nella volontà e nel piglio morale, la società circostante
                                mostra come la schiavitù nella casa e la schiavitù nello stato siano una cosa sola. Le
                                donne assoggettate nell'harem del Sultano indicano altresì quanto l’Oriente rivesta una
                                doppia valenza per Montesquieu. Se da un lato esso rappresenta lo straniamento con cui
                                guardare ai vizi e alla corruzione dell'Europa, dall'altro esso è pietra di paragone che
                                indica quanto il disordine che regna nelle forme di stato assoluto europee preluda ad un
                                “movimento”, ad un possibile cambiamento, una condizione preferibile alla stasi di un
                                mondo orientale visto quasi come il terreno naturale non tanto del dispotismo, quanto
                                dell'immutabilità del dispotismo stesso.
                            </p>


