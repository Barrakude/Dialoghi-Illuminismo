                            <p class="a p-3">Dal silenzio alla ribellione. La figura femminile nel romanzo francese</p>
                            </br>
                            <p class="p-3">
                                Tra i romanzi francesi in cui la figura femminile assume un rilievo del tutto
                                particolare, due esempi, tra tutti, porgono alla riflessione critica temi e problemi di
                                drammatica rilevanza, che segnano sia il primo Settecento sia il periodo del declino dei
                                Lumi: Les Liaisons dangereuses (Le relazioni pericolose 1782) di Laclos e le Lettres
                                persanes (Lettere persiane 1721) di Montesquieu. Scegliendo uno stesso genere, il
                                romanzo epistolare, i due scrittori delineano condizioni femminili che riguardano solo
                                apparentemente l'esteriorità del vivere associato e che in realtà riflettono problemi
                                ben più seri di morale individuale e collettiva, nei rapporti tra i sessi. La Marchesa
                                di Merteuil, protagonista de Le relazioni pericolose, e Roxane, una delle rare figure
                                femminili delle Lettere persiane, hanno parecchie cose in comune, anche se appartengono
                                ad ambienti diversissimi tra loro. La prima si situa nell'ambiente frivolo e cortigiano
                                della società parigina. Il suo status sociale di donna libera è ben diverso dallo status
                                della schiava Roxane che appartiene all' harem del suo signore, il sultano Usbek, di cui
                                è la favorita. Eppure entrambe rappresentano una condizione femminile soggiogata dai
                                mille pregiudizi sull'onore e sul comportamento e cui, di fatto, è negata la libertà di
                                pensare. Eroine della volontà, esse sanno trarre la loro superiorità intellettuale dalle
                                maschere che cinicamente oppongono ad uno sguardo altrui che le giudica attraverso la
                                lente del pregiudizio. Entrambe si ribellano ad un'educazione che consegna la donna al
                                silenzio e alla passività, imparando l'“arte della dissimulazione”. Entrambe capiscono
                                quanto il pensiero sia il loro unico bene. Un bene esclusivo, che va conquistato con
                                paziente abnegazione giorno per giorno, e poi continuamente protetto, coltivato
                                sottomanto, più spesso dissimulato e nascosto che esibito. Esse imparano a mentire non
                                tanto per soddisfare l'orgoglio, quanto più l'intimo bisogno di libertà, in una vita
                                sociale che esse non possono interpretare che come lotta e difesa contro la
                                sopraffazione. Una lotta intellettuale, prima ancora che sentimentale ed emozionale, in
                                cui la duplicità è presentata come una sofferta arte di vivere, nella commedia mondana
                                che condanna la donna all'alienazione e che definisce “virtù” la sua sottomissione. A
                                null'altro destinata se non a compiacere l'altro sesso, la donna non può che
                                sottomettersi o ribellarsi, in un secolo in cui anche un filosofo anticonvenzionale come
                                Jean-Jacques Rousseau afferma: <br><br> <em>
                                    “La donna è fatta soprattutto per piacere all'uomo. Se l'uomo deve a sua volta
                                    piacerle, è una necessità meno diretta: il suo merito sta nella forza; egli piace
                                    per il fatto stesso che è forte. Se la donna è fatta per piacere e per essere
                                    soggiogata, deve rendersi gradevole all'uomo anziché provocarlo”.</em> <br><br> Ed è
                                ad una ribellione intesa soprattutto come amaro e lucido cammino di conoscenza che si
                                applicano le eroine immaginate dai due autori francesi, spiegandone le ragioni nelle
                                lettere indirizzate ai loro ignari compagni (Lettera al visconte di Valmont e Lettera di
                                Roxane a Usbek).

                            </p>


