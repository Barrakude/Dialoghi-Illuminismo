                            <p class="p-3">
                                Ne I legami pericolosi, una lettera della protagonista, la Marchesa di Merteuil,
                                denuncia l'educazione coercitiva che, differenziando i sessi, vota la donna “al silenzio
                                e all'inazione”, negandole sia lo sviluppo del pensiero e della ragione sia la libertà
                                di comportamento. Intuendo che una gioventù sinonimo di sottomissione sarebbe stata
                                l'unica condizione dell'esistenza femminile, la Marchesa di Merteuil spiega come,
                                adolescente, le sue energie si indirizzassero a dissimulare il pensiero che, se
                                evidenziato, le sarebbe stato carpito, conculcato.
                                Intenta a nascondere i talenti che andava sviluppando, imponendosi un'osservazione
                                attenta dei comportamenti e dei discorsi altrui, sotto la maschera della distrazione e
                                della vanità, ella rivendica l’ascesi stoica, vera e propria disciplina imposta,
                                esercizio paziente di tutte le sue facoltà fino alla conquistata padronanza di sé. La
                                cerebralità dei suoi atti le fa orgogliosamente affermare di aver sconfitto le
                                determinazioni sociali che pesano sulla donna, opponendo la libertà umana di obbedire ai
                                propri valori anziché a quelli del gruppo sociale. La conquistata consapevolezza di sé
                                le farà dire: “sono io l'opera di me stessa”.

                            </p>

