                            <p class="a p-3">Le figure femminili nei romanzi di Defoe e Richardson
                                </br>
                            <p class="p-3">
                                In Inghilterra Defoe, che dà inizio ad un nuovo modo di scrivere, stabilendo il primato
                                dell'esperienza individuale nel romanzo, rappresenta una condizione femminile molto
                                lontana dai modelli della morale pubblica. Inedita e del tutto originale è infatti
                                l'esperienza della sua Moll Flanders, con la quale l'autore condivide un'infanzia
                                poverissima e l'inevitabile, sofferta, visione dell'esistenza come lotta quotidiana per
                                la sopravvivenza. La conquista delle ricchezze e degli agi frutto di malefatte e
                                turbolenze da parte di Moll -Moll povera o ricca è una ladra senza scrupoli- è vista con
                                un sorriso di soddisfazione per aver scampato la sorte dei suoi compagni di strada e
                                cioè la forca. Nella lunga autoconfessione che costituisce la forma del romanzo, Moll
                                appare come un'eroina scanzonata, ricca di esperienza. Nasce a New gate da una
                                prostituta e ladra deportata, attraversa vicissitudini diverse, anche drammatiche, ma
                                mai vissute con drammaticità. È a dieci anni una donnina modesta e garbata che aspira a
                                ricoprire un ruolo di “signora” per assicurarsi l'indipendenza economica, a diciotto
                                anni, giovane donna di modestissima virtù, ha ben capito che “il mercato (del
                                matrimonio) è nelle mani degli uomini”, dà quindi sfrontati consigli: “una donna non
                                deve mai perdere l'occasione di vendicarsi di un uomo che l'ha trattata ingiustamente”,
                                “non prendere il primo buon cristiano che capita”, non “precipitarsi nel matrimonio”, sa
                                essere all'occorrenza madre devota oltre che amante appassionata, in ogni cosa si butta
                                con coraggio, assumendone i rischi. Fiera, è soprattutto una donna che sa costruire la
                                sua indipendenza senza mai vergognarsi, né tantomeno pentirsi delle malefatte con cui ha
                                raggiunto quella meta. Sia per Defoe che per Richardson la protagonista e lo scenario
                                delle sue azioni vengono collocati in una nuova prospettiva letteraria: la trama è fatta
                                da personaggi particolari, ritratti in precise circostanze, piuttosto che da tipi umani
                                generali su uno sfondo determinato dalle convenzioni letterarie. Lo scenario su cui si
                                muovono i personaggi è costituito da descrizioni dettagliate degli oggetti e dallo
                                spostamento da uno scenario naturale agli interni. La ricerca della verosimiglianza
                                porta il romanziere ad inserire il personaggio nell'ambiente fisico e sociale di
                                appartenenza. In Moll Flanders il denaro viene contato, la biancheria elencata. Molto
                                diversamente, le residenze in cui abita Pamela sono vere e proprie prigioni e la casa di
                                Clarissa Harlowe è uno sfondo che assume un significato morale, fisico, terribilmente
                                reale.
                            </p>

