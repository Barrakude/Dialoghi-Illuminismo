                        <h2>Tra Parigi, Londra e Ispahan: eroine di testa e di cuore.</h2>
                        <div class="article-preview p-3">
                            <p class="p-3">Il cosmopolitismo settecentesco trova nel romanzo il proprio luogo di
                                elezione. Genere
                                ibrido, che non rispetta le unità di tempo e di luogo, il romanzo consente di dipingere
                                realtà geografiche diversissime tra loro, di evocare città e paesi reali o immaginari,
                                cui si imprime carattere di esemplarità, su cui si esercita la riflessione. Lo scopo
                                sotteso non è il puro divertimento del lettore, ma la sua istruzione. Questi si abitua a
                                confrontare i costumi, le idee, le filosofie, impara a discutere, a contraddire, a
                                dubitare, acquisisce il senso del relativo. Il romanzo, non diversamente dagli altri
                                generi, lo costringe ad interrogarsi e riflettere, ponendo questioni cruciali:<br><br>
                                <em>"Quale
                                    società, meglio di altre, quale legge morale, quale religione ed istituzione
                                    consente, se non il raggiungimento, almeno l'approssimazione alla verità e alla
                                    felicità, sotto i diversi climi che i viaggi vanno via via scoprendo? Come si devono
                                    concepire le relazioni sociali, come i rapporti tra uomo e donna e che posizione
                                    deve occupare quest'ultima all'interno di una comunità?"</em> <br><br>

                                La donna e i suoi ruoli,
                                l'educazione, i rapporti tra uomo e donna, problema che inquieta la coscienza
                                europea sotto tutte le latitudini, diventano così un tema privilegiato del romanzo
                                di quest'epoca, mentre Francia, Inghilterra, Persia non sono solo pittoreschi sfondi
                                di improbabili vicissitudini. Più in generale Oriente e Occidente sono le grandi
                                metafore con cui guardare a civiltà, ciascuna delle quali si considera come la sola
                                perfetta, e che, se messe a confronto, proiettandone le diverse condizioni di
                                sviluppo, consentono di osservare verità che non possono più essere considerate
                                uniche ed esclusive. Significativi sono a questo riguardo i romanzi degli inglesi
                                Defoe e Richardson e dei francesi Montesquieu e Laclos. Colta nella Londra di Defoe,
                                di Richardson o nella Parigi e nella Ispahan di Montesquieu, la figura femminile fa
                                risuonare i diversi “milieux” con l’appartenenza sociale e la diversità di “status”.
                                I romanzi di Defoe e Richardson appartengono allo stesso periodo cronologico (1722
                                Moll Flanders, Pamela e Clarissa 1741 e 44) delle Lettere persiane di Montesquieu e
                                cioè la prima metà del secolo. Eppure, se si eccettua una pari forza immaginativa
                                nel dar vita alla figura femminile e un'analogia tematica che colloca l'educazione
                                della donna al centro di significativi episodi nell'universo narrativo, si possono
                                osservare le profonde disomogeneità di visione, di stile, di contenuto relative alle
                                diverse condizioni di sviluppo e alle diverse movenze della società inglese rispetto
                                a quella francese. Se la Moll Flanders di Defoe si disfa innanzitutto dei pregiudizi
                                sociali che collimano con i principi di “onore”, “decoro” e “status”, le eroine di
                                Richardson, che nel pregiudizio si dibattono fino all'umiliazione di sé, conquistano
                                soprattutto il diritto all'integrazione sociale in una classe diversa da quella
                                d'origine, anziché il più cerebrale diritto alla propria indipendenza e libertà,
                                mentre i personaggi femminili di Montesquieu e di Laclos appaiono come
                                l'incarnazione del modello maschile del “philosophe”. Eroine di testa e di cuore -
                                quest'ultimo termine deve essere colto nell'accezione di «coraggio» -, esse si
                                esercitano nel pensiero, oltre che nel difficile, spesso cinico, esercizio di
                                un'autoeducazione che si emancipa dal pregiudizio sociale, uscendo dai confini
                                dell'altrui psicologia.
                            </p>


