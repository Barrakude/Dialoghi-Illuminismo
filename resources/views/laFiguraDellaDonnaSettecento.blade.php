                            <p class="a p-3">L'educazione della donna. Dalle <em>Lettere persiane …
                                </em>
                            </p>
                            </br>
                            <p class="p-3">
                                Costume, legge, giustizia, politica, le Lettere persiane affrontano temi di vivace
                                attualità, ma il filo rosso che tiene insieme le idee dall'inizio alla fine del romanzo
                                epistolare è il tema da cui ha origine la narrazione stessa: i rapporti tra i sessi.
                                Nella prima lettera dall'Europa, un'osservazione dei Persiani appena sbarcati a Livorno
                                orienta il discorso sui costumi europei: «Le donne vi godono una grande libertà»
                                (XXIII). Nella seconda lettera inviata da Parigi, si parla della rivolta gallicana
                                contro la bolla Unigenitus e si precisa: «Fautrici di questa rivolta […] sono state le
                                donne» - cui la bolla proibiva la lettura della Bibbia - «indignate per l'oltraggio
                                fatto al loro sesso […] sollevarono tutti contro la Costituzione». Più avanti nel
                                romanzo epistolare il tono inconfondibile della Reggenza, un misto di cavalleresca
                                galanteria e humour, mette in discussione e in ridicolo il dominio maschile in generale,
                                con ragionamenti molto avanzati rispetto ai tempi e che restano senza eguali fino al
                                sérieux di Condorcet, e fino alle ultime fasi dell'Illuminismo: <br><br><em>

                                    «Un'altra questione è sapere se la legge naturale assoggetta le donne agli uomini.
                                    “No,
                                    -mi diceva l'altro giorno un galante filosofo, - La natura non ha mai dettato una
                                    tale
                                    legge. L'impero che noi abbiamo su di esse è una vera tirannia; esse ce lo hanno
                                    lasciato prendere perché sono più dolci di noi, e per conseguenza più umane e
                                    ragionevoli…Perché dovremmo avere un privilegio? Forse perché siamo più forti? Ma è
                                    una
                                    vera ingiustizia. Noi impieghiamo ogni sorta di mezzi per fiaccare il coraggio delle
                                    donne: le forze sarebbero uguali se fosse uguale l'educazione. Mettiamole alla prova
                                    nelle capacità che l'educazione non ha indebolito e vediamo se siamo così forti».
                                </em>
                                (Usbek,
                                Lettera XXXVIII) <br><br>

                                Ma benché lucidamente critico su questi temi come sulle nefandezze dell'assolutismo
                                europeo, il “saggio” Usbek non sa riconoscere le più tetre forme del dispotismo
                                orientale di cui egli stesso è responsabile. Nell' harem che ha lasciato per venire in
                                Europa, le sue schiave-amanti vivono in segregazione, “custodite” da eunuchi cinici e
                                spietati. Capirà da una drammatica lettera della sua favorita Roxane, la sofferenza
                                femminile e le reali condizioni di vita dell’harem. Montesquieu ambienta poi il fosco
                                dramma della morte di Roxane nella ferrea cornice di rapporti di forza che paiono
                                immutabili. L'immaginario “orientale” dell'autore si capovolge alla fine del romanzo.
                                L'oriente non è più immaginato in funzione dello “straniamento”, per portare uno sguardo
                                critico sui costumi occidentali, ma è qui proposto come metafora di un potere politico
                                più cupo ed aspro del potere occidentale. Il dispotismo orientale, con il suo
                                immobilismo, la sua immutabile stazionarietà, appare a Montesquieu peggiore
                                dell'assolutismo europeo. Questo è certo segnato dal caos, dal disordine, ma dal caos
                                può nascere il movimento. Un certo progresso delle idee, lascia intravedere una
                                possibilità di cambiamento. Si vede come Montesquieu riproponga quella coppia di
                                opposti: "progresso" versus "stazionarietà" in continuità con le linee sulle quali si
                                sta costruendo, proprio per differenza con l'oriente, l'ideologia europea.</p>

