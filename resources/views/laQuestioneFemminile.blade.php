                        <h2>Rivoluzione e diritti della donna</h2>
                        <div class="article-preview p-3">
                            <p class="p-3">Il periodo che precede la Rivoluzione è fortemente caratterizzato dalla
                                presenza di
                                élites femminili in quasi tutti gli stati europei. Benché a fatica, poche donne riescono
                                ad accedere all’istruzione, più spesso da autodidatte, e a riflettere sulla loro
                                condizione sociale. Esse propongono riforme legislative a favore del genere femminile,
                                degli oppressi, degli schiavi (LINK OLYMPE DE GOUGES). Uno dei nuclei centrali del loro
                                pensiero sta nella convinzione che «qualsiasi rinnovamento, qualsiasi miglioramento
                                della società sarà possibile solo ove le donne, liberandosi dai pregiudizi che per
                                secoli e secoli ne hanno limitato la formazione, l’acculturazione, la possibilità di
                                partecipare alla vita pubblica, acquistino la consapevolezza di essere membri partecipi
                                della società e protagoniste del suo progresso». Ma in un mondo ad egemonia maschile le
                                loro elaborazioni concettuali e i loro scritti non sono riconosciuti e bisognerà
                                attendere epoche più recenti per la comprensione del valore politico delle loro opere.
                                La cesura rivoluzionaria comporta esiti contraddittori. Se Jean Antoine de Condorcet in
                                Sur l’admission des femmes au droit de cité affronta il problema dell’esclusione delle
                                donne dal diritto di cittadinanza, riassumendo molte delle posizioni illuministiche del
                                secolo sulla questione femminile, Restif de la Bretonne sostiene che la lettura e la
                                scrittura devono essere vietate alla donna, per non distrarla dai più utili lavori
                                domestici. Di fatto, l’immagine della donna elaborata dalla Rivoluzione è in netto
                                contrasto con gli avanzamenti teorici del secolo. Il modello repubblicano di figura
                                materna, molto vicino a quello elaborato nell’Emile di Jean-Jacques Rousseau (LINK
                                ROUSSEAU EMILE SOPHIE), porta alla pubblica riprovazione la condotta e i costumi delle
                                donne che rifiutano di adeguarsi alla loro «natura» di madri e nutrici. Le teorie
                                femministe diffuse attraverso i pamphlets, che iniziano ad apparire dal 1787, formulano
                                proposte specifiche riguardanti l’istruzione, i diritti civili e politici, ma non
                                approdano a concrete, sostanziali riforme. Tra i più importanti, il Cahier des doléances
                                et réclamations des femmes, pubblicato da un’anonima autrice con la sigla: M.me B.B.,
                                afferma che, come un aristocratico non può rappresentare un popolano nelle assemblee,
                                allo stesso modo un uomo non può rappresentare una donna. Ma l’attribuzione della
                                cittadinanza, così come quella dei diritti politici, è riservata ai soggetti maschi,
                                bianchi e la donna deve tornare ad esprimere il suo dissenso. Sarà Olympe de Gouges
                                (1748-1793) a redigere, nel 1791, una delle espressioni più radicali e moderne
                                dell’uguaglianza dei sessi: La Déclaration des Droits de la Femme, in cui rivendica
                                l’estensione alle donne della Dichiarazione dei Diritti dell’uomo del 1789 e chiede
                                parimenti l’uguaglianza delle opportunità di accedere alle cariche pubbliche. I tempi
                                erano maturi, anche se l’arretratezza delle mentalità sembrava non recepire i grandi
                                mutamenti che si affacciavano alla ribalta della storia ad opera delle donne.
                                Oltremanica, l’azione di Olympe ha come corrispettivo A Vindication of the Rights of
                                Woman, opera pubblicata nel 1792 da Mary Wollstonecraft, un’altra strenua sostenitrice
                                dei diritti delle donne che, diversamente da Olympe, non paga le sue posizioni con la
                                vita, ma con gli scherni e le offese, mentre le sue teorie sulle donne e le sue scelte
                                esistenziali costituiscono una continua provocazione per il perbenismo dell’epoca.
                                Singolare personalità di autodidatta militante, Olympe de Gouges non ha nulla della fama
                                né dell’educazione alle lettere delle intellettuali sue contemporanee, come Mme de
                                Staël, o Manon Roland. Scrive con difficoltà, riconoscendo lucidamente gli errori di
                                stile e le lacune culturali. Eppure né le carenze che onestamente rivela, né le
                                insinuazioni dei contemporanei - Beaumarchais attribuiva la stesura dei suoi scritti ai
                                suoi amanti - le impediscono di contenere la significativa produzione cui dà vita tra il
                                1780 e il 1793, anno della sua esecuzione. Oltre ai testi-chiave che avranno risonanza
                                mondiale, come la già citata dichiarazione dei Droits de la Femme e L’Esclavage des
                                Nègres, firma una dozzina di pièces teatrali che costituiscono l’emblema della lotta
                                contro tutte le ingiustificabili forme di oppressione. Diritti delle donne e diritti dei
                                neri sono rivendicati in nome di quel concetto di Natura mutuato da Rousseau, che la de
                                Gouges rielabora, erigendolo a fondamento di un diritto naturale universale che non
                                tollera gerarchie o distinzioni di religione, colore, sesso fra gli esseri umani. Il
                                Novecento riconosce come cruciale il percorso di questa scrittrice, non solo per quanto
                                riguarda la chiarezza con cui formula il tema dell’emancipazione civile e politica della
                                donna, ma anche perché i suoi testi rimangono un documento che lascia intravedere le
                                grandi svolte della storia (cfr. AA.VV. Storia Europea della Letteratura Francese,
                                Torino, Einaudi, 2013; A. Loche, La Liberté ou la mort. Il progetto politico e giuridico
                                di Olympe de Gouges, Modena, Mucchi Editore, 2021).

