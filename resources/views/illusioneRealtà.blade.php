                        <h2>Il paese delle chimere</h2>
                        <div class="article-preview p-3">
                            <p class="p-3">La riflessione sul motivo delle illusioni appartiene all'essenza stessa
                                della letteratura e tuttavia spetta proprio al secolo dei lumi, il secolo che intende
                                celebrare la supremazia della ragione, la responsabilità di fornirne una prima
                                definizione articolata in senso moderno. Fino a questo momento, infatti, l'aspirazione
                                dell'intelligenza umana a sollevarsi oltre i confini delle verità accertate si era
                                nutrita, pur con intensità e sensibilità diverse, delle seduzioni fantastiche della
                                mitologia classica o dei messaggi edificanti dell'immaginario cristiano.
                                Il merito, o la novità, del Settecento consiste appunto nella volontà di laicizzare
                                questa aspirazione allontanandola dai vincoli rigidi dei sistemi ideologici ed estetici
                                consolidati per trasformarla in occasione di interpretazione critica della realtà
                                contemporanea.
                                In questa prospettiva l'illusione subisce un processo di ridefinizione che la porta ad
                                assolvere a molteplici e diverse funzioni ideali ; proposta, in alcuni casi, come
                                ricomposizione del nascente dissidio romantico tra io e mondo, diventa, in altri,
                                occasione di sollievo e autoinganno ; offerta come strumento di denuncia e di rigetto,
                                si traduce, al contrario, in fuga in un sovramondo astratto e consolatorio.
                                E' Jean-Jacques Rousseau, con il romanzo Giulia o la nuova Eloisa, a porre le basi di
                                questo dibattito attraverso l'indagine del processo di formazione di una originale
                                eroina letteraria. La scelta di Giulia, la protagonista, che rinuncia al suo amore per
                                il giovane Saint-Preux non è semplicemente la riconferma, celebrata nell'opera
                                secentesca di Mme de La Fayette : La principessa di Clèves (1678), della subordinazione
                                del desiderio alle convenzioni ma l'occasione per nuove riflessioni e nuove opportunità
                                di indagine. Non potendo vivere secondo la propria aspirazione naturale ella si adegua
                                alle possibilità della sua esistenza e sublima la propria passione in una ricerca
                                dell'equilibrio che utilizzi le armi della ragione e del controllo ; queste armi
                                tuttavia, se giustificano il sereno equilibrio con il marito e l'utopia di Clarens, non
                                riescono a cancellare la presenza del passato.
                                Per annullarla è necessaria una risposta nuova, oltre i limiti imposti dall'uso
                                controllato del raziocinio : il riconoscimento, cioè, che l'illusione è l'unica emozione
                                che garantisce la percezione della felicità ; solo il "paese delle chimere" ha senso che
                                sia abitato perchè tutto ciò che è reale implica lo schianto della disillusione ; il
                                possesso, qualora venga anche conseguito, non cesserà di manifestare la sua
                                provvisorietà e la sua incompletezza. La soluzione è tuttavia lungi dall'essere
                                serenamente conquistata; l'abbandono al sogno apre lo spazio alla noia, al vuoto,
                                all'inquietudine, ovvero ad una condizione di assenza che Giulia sembra superare solo
                                con la riscoperta di un Dio tollerante e clemente. Un Dio, cioè, lontano da ogni
                                suggestione mistica, capace anche di tacere quando Giulia, al limitare della morte,
                                nella sua ultima lettera smentirà ogni sua consolatoria conclusione confessando
                                all'amato di non aver mai smesso di desiderarlo.
                                L'epilogo "aperto" e dunque moderno della Nuova Eloisa non rappresenta comunque
                                l'approdo definito del pensiero del filosofo ginevrino. Nelle Fantasticherie del
                                passeggiatore solitario, testimonianza della sua vecchiaia, la funzione liberatoria
                                delle illusioni viene radicalmente negata. L'autentica percezione della vita non
                                appartiene né ad un passato smarrito nell'incertezza dei ricordi né ad un futuro
                                inutilmente atteso. Vive, al contrario, nel presente, nella consapevole immersione nelle
                                bellezze e nelle sensazioni offerte dalla natura,nel godimento delle esperienze offerte
                                dalla conoscenza dei sensi attraverso la piena condivisione del "sentimento
                                dell'esistenza". <br><br>
                                Le tesi di Rousseau sollecitarono un dibattito e una attenzione straordinaria. Tra gli
                                altri, sulle sue orme, si mosse il giovane Goethe, che nel comporre I dolori del giovane
                                Werther, ritorna all'epilogo della Nuova Eloisa. L'indisponibilità ad adeguarsi alla
                                realtà di una esistenza banale oppressa dalle strutture statiche dell'ancien régime
                                spinge, anche in questo caso, l'eroe a rifugiarsi in un mondo interiore dominato dalla
                                evocazione silenziosa dell'illusione ; ma lungi dall'offrire una soluzione positiva o
                                consolatoria, l'illusione si traduce in una assenza che sfugge dai parametri della
                                definizione filosofica per realizzarsi, in coerenza con le coordinate ideologiche ed
                                estetiche dello Sturm und Drang, attraverso le presenze simboliche della natura. Il
                                giovane Werther si smarrisce nell'idillio, nelle atmosfere solitarie di una campagna
                                solidale accompagnato soltanto dalla incontaminata e pura lettura di Omero. La sua,
                                tuttavia, non è un'immersione salvifica ma una inconsapevole propensione alla fuga
                                ineluttabilmente destinata a scontrarsi con il proprio fallimento. Separato dalla
                                società Werther gode effettivamente di una "meravigliosa serenità" ma quando tornerà ad
                                aprirsi al mondo non potrà che incontrare le radici delle proprie contraddizioni. In
                                primo luogo l'impossibilità di dialogare ed essere compreso, persino dalle persone che
                                ama e stima. Lotte, per quanto adorata, interpreta un ideale umano consapevole dei
                                limiti del desiderio e della necessità del compromesso; il suo equilibrio, la
                                consapevolezza del suo ruolo, la sua accettazione della condizione borghese ne fanno
                                oggetto di desiderio ma anche di distanza; Alberto, l'amico cercato e non trovato,
                                l'antagonista disperatamente negato, non comprende e accentua, nella differenza, la
                                condizione separata dell'eroe. Werther si trova inquietantemente solo e isolato; la sua
                                condizione di ribelle e di diverso esasperata. Il ritorno alla natura sarà a questo
                                punto impossibile; prosciugato nello spirito, contraddetto nelle sue emozioni più
                                profonde non potrà che attraversare i luoghi a lui più cari per constatarne l'ostile
                                avversità o per ascoltarne il silenzio indifferente prima di affrontare il proprio
                                ineluttabile destino.
                                La medesima vocazione tragica ritorna, alla fine del secolo, nel "libro del cuore" di
                                Ugo Foscolo, Ultime lettere di Jacopo Ortis. La crisi storica determinata
                                dall'esplosione della Rivoluzione francese e il progressivo estinguersi della fiducia
                                negli strumenti interpretativi dell'Illuminismo, determinano tuttavia una riscrittura
                                originale del senso dell'illusione ormai chiaramente proiettata verso l'Ottocento
                                romantico. Per Jacopo Ortis, la vittima sconfitta dell'utopia rivoluzionaria, ogni
                                possibilità è già assolutamente negata e le proprie consapevolezze, dunque, non possono
                                che evocare insistentemente la morte. L'illusione, al più, appare come una incerta
                                volontà di sopravvivenza, contraddetta dagli eventi ma prometeicamente ricercata. In
                                primo luogo essa assume il profilo tradizionale dell'amore attraverso i lineamenti di
                                Teresa, la divina fanciulla, di cui l'eroe si innamora senza speranza ; emblema di
                                bellezza, armonia, purezza si rivela essere la metafora di un mondo perduto ; la sua
                                dimensione umana cede rapidamente il posto ad un ideale estetico che si immedesima di
                                fatto con il mito classico. Teresa, più precisamente, non è affatto un'eroina romanzesca
                                condannata ad un matrimonio di interesse ; è, in realtà, un modello di bellezza poetico
                                che agisce contro la disarmonia del reale. In quanto illusione perduta e
                                irraggiungibile, dunque, essa finisce per rappresentare l'esemplificazione di una
                                ricerca condannata al fallimento.
                                Non diversamente naufraga la speranza nella solidarietà idillica nella Natura.
                                L'influenza di Werther è consistente ; anche Jacopo cerca sollievo nelle atmosfere
                                accoglienti della campagna ; anche Jacopo placa le sue inquietudini rispecchiandosi in
                                una natura "fonte inesausta di piacere" ; eppure, così come era accaduto all'angosciata
                                creatura di Goethe, la natura non appare affatto un'entità capace di sublimare la sua
                                condizione esistenziale ma l'interprete indifferente dell'eterno fluire della materia.
                                Jacopo può certamente sdraiarsi a contemplare "la cima del monte indorato da' pacifici
                                raggi del sole" e ascoltare il flebile canto degli uccelli o il muggito bucolico delle
                                giovenche, eppure è cosciente dell'inattendibilità di questa pace. La Natura autentica,
                                quella capace di mostrare il suo volto più crudele ha il volto spaventoso delle rocce
                                scoscese, dei sentieri dirupati, dei precipizi che accompagnano, romanticamente, la sua
                                decisione di uccidersi.


