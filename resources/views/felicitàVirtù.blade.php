                            <p class="a">La cultura della felicità in Francia nel ‘700</p></br>
                            <p>
                                La cultura della felicità ha uno sviluppo importante in Francia nella prima metà del
                                ‘700; nasce una trattatistica di tale ampiezza che si è potuto parlare di una vera
                                «ossessione» (così l’ha definita lo studioso francese Robert Mauzi), condivisa da
                                moralisti (atei e cristiani), libertini, filosofi. Viene esaltata la felicità che
                                ciascuno può trovare in questo mondo, senza negare o umiliare la sua natura sensibile,
                                la ricchezza delle sue capacità, la varietà delle sue potenzialità. Si insiste inoltre
                                sulla disposizione degli uomini alla virtù, all’azione utile al bene della società, da
                                cui verrebbe anche quel tipo superiore di piacere, fatto di riconoscimento altrui e di
                                stima di sé, che rappresenta una condizione fondamentale della felicità individuale.
                                Questa trattatistica viene recepita in alcuni articoli dell’Encyclopédie, come nella
                                voce “Bonheur”. Parzialmente fuori dal coro è invece la voce di La Mettrie, autore di un
                                discusso Discorso sulla felicità (1748): fisiologo e materialista, egli polemizza con le
                                filosofie della moderazione, rivendicando la naturalità di ogni piacere e
                                l’impossibilità di stabilire tra essi una gerarchia di valore. Tutti convergono,
                                comunque, nel sostenere che la felicità non consiste solo nella fuggevole esperienza del
                                piacere, ma in una condizione durevole di benessere interiore e contentezza di sé, di
                                cui ciascuno è giudice esclusivo. Da questo punto di vista la felicità resta un fine cui
                                tendere, piuttosto che una realtà raggiungibile. A contrastare l’ottimismo dell’epoca,
                                intervengono importanti figure di intellettuali, come Maupertuis e Voltaire, che tornano
                                a sottolineare la presenza dominante del dolore e del male nell’esistenza umana,
                                presenza che rende illusoria anche solo l’idea di poter raggiungere una condizione di
                                stabile felicità. Oltre a ciò molti sottolineano il potere ingannatore
                                dell’immaginazione la facoltà che alimenta i progetti umani, ma li espone continuamente
                                alla disillusione. In tutti i casi, i trattati francesi della prima metà del ‘700 sono
                                rivolti a individui appartenenti ad un’élite intellettuale, che hanno tempo e
                                disponibilità per prendersi cura di sé e coltivare il proprio sogno di felicità. Sullo
                                sfondo rimangono i problemi sociali che escludono la maggioranza degli uomini dalla
                                possibilità di godere di condizioni minime di benessere e dignità. Nella seconda metà
                                del secolo, invece, la disuguaglianza sociale comincia a diventare una questione
                                politica rilevante. Molti intellettuali, come Rousseau, Helvétius e d’Holbach, si
                                sentono investiti del problema di pensare come sia possibile ridurre le condizioni di
                                palese sofferenza e umiliazione della maggior parte della popolazione e di costruire una
                                forma di civiltà in cui gli uomini possano sentire il bene comune come bene proprio. In
                                una società lacerata da disuguaglianze sempre più drammatiche si impone così il tema
                                della felicità pubblica di cui dovrebbero essere garanti l’autorità pubblica e il
                                legislatore. A fine secolo, chiusa senza esiti concreti la stagione della teoria e
                                dell’utopia, saranno la storia e la rivoluzione a imporre in Francia come nuova parola
                                d’ordine: il diritto alla felicità.
                            </p>


