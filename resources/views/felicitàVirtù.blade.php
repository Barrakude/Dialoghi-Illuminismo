                            <p class="a">Felicità e virtù nel dibattito inglese del ‘700</p></br>
                            <p>
                                Anche in Inghilterra si dibatte sulla felicità a partire dalla sfida portata da Hobbes,
                                che aveva indicato nell’amore di sé e nel desiderio di potere i motivi dominanti
                                dell’agire individuale e i soli criteri di spiegazione del sistema di relazioni.
                                Filosofi come Shaftesbury, Hutchison e Hume dedicano particolare attenzione al legame
                                morale tra individui e società nella convinzione che gli uomini possano ricercare la
                                loro felicità senza diventare né egoisti né diffidenti gli uni verso gli altri. Molte
                                ricerche si indirizzano a mostrare l’esistenza di disposizioni naturali alla simpatia,
                                alla reciprocità e alla benevolenza; la società viene dipinta come il frutto di
                                un’evoluzione improntata all’incremento della socievolezza; l’uomo civilizzato appare
                                desideroso della buona reputazione e incline ad approvare comportamenti giusti e
                                socialmente utili. I pensatori inglesi e scozzesi condividono anche, in linea di
                                massima, l’idea che, per avere un buon ordine sociale, non sia necessario un intervento
                                politico che determini ciò che è bene per gli individui (tema centrale invece nella
                                tradizione francese): civiltà e progresso sarebbero il frutto di dinamiche spontanee.
                                L’idea è condivisa, da prospettive divergenti, sia da Mandeville che da Adam Smith: il
                                primo, provocatorio autore della celebre Favola delle api (1705-1729), sottolinea, con
                                forte effetto di paradosso, come a produrre realmente il benessere della società non
                                siano le azioni virtuose, ma i vizi degli uomini; il secondo intende invece mostrare in
                                che modo uomini guidati dalla prudenza e dall’oculatezza, nella cura del proprio
                                interesse, possano produrre un sistema sociale ordinato e moralmente buono (Teoria dei
                                sentimenti morali, 1759). Il francese Montesquieu è in totale disaccordo con Mandeville.
                                Nelle sue Lettere persiane, un romanzo epistolare, egli inserisce un apologo nel
                                romanzo, I Trogloditi, con cui esemplifica come il vizio, l’egoismo e la malvagità
                                portino i popoli alla distruzione. Alla fine del secolo, con l’Introduzione ai principi
                                della morale e della legislazione (1789), Jeremy Bentham dà inizio ad una nuova
                                tradizione filosofica: l’utilitarismo. Questa teoria etica considera calcolabile
                                oggettivamente la quantità di bontà sociale di un’azione: un’azione è utile, e per
                                questo moralmente buona, se aumenta la felicità del maggior numero di persone; e la
                                felicità collettiva, di cui si deve prendere cura il legislatore, è determinabile in
                                base alle preferenze individuali in termini di beni e piaceri, dal momento che tutti gli
                                uomini perseguono, tendenzialmente, gli stessi interessi. Si tratta di una tradizione di
                                pensiero destinata a influenzare profondamente, fino ai giorni nostri, le teorie
                                economiche, sociali e politiche. Dagli intensi dibattiti del ’700 scaturisce l’idea che
                                la politica debba occuparsi di felicità, sia in termini di garanzia per gli individui,
                                sia di attiva promozione del bene pubblico, attraverso azioni di governo. Il diritto
                                alla felicità viene codificato per la prima volta nella Dichiarazione di Indipendenza
                                americana del 4 luglio 1776 ed entra poi nel dibattito interno alla rivoluzione
                                francese. I giacobini ne fanno il perno della loro politica dirigista, rivendicando il
                                primato della «felicità comune» su quella individuale.</p>



