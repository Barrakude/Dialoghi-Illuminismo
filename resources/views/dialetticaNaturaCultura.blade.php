                        <h2>La legge di natura da Hobbes a Rousseau</h2>

                        <div class="article-preview p-3">
                            <p>Raccogliendo l'eredità del pensiero critico tardo-cinquecentesco e secentesco, di quei
                                filoni tematici che vanno da Montaigne a Bayle, la riflessione degli illuministi dà
                                impulso ad un ampio dibattito, che trova nella dialettica natura/cultura il proprio
                                fulcro problematico. Presente in svariati campi del sapere, l'idea di una legge di
                                natura che rende gli uomini liberi ed uguali nei diritti, si configura come vera e
                                propria dottrina nell'ambito degli studi giuridici, filosofici, politici. Sebbene con
                                motivazioni e cadenze diverse, e talora opposte, essa viene ripresa, tra il XVI e il
                                XVIII secolo, da Grozio, Hobbes, Locke, Montesquieu, Genovesi, giungendo fino ad
                                ispirare la letteratura, l'arte, la pedagogia e, negli esiti contemporanei, gran parte
                                degli studi antropologici ed etnologici, da Rousseau, fino a Lévi-Strauss. Le
                                innumerevoli costellazioni di questo tema: stato di natura, uomo naturale, legge
                                naturale, insieme ai suoi derivati: uomo selvaggio, cannibale, barbaro, sono da leggersi
                                nel rapporto di opposizione che tali termini mantengono con stato civile, uomo
                                civilizzato, cultura, diritto positivo, uomo in società.</p>


