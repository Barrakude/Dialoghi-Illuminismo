                            <p class="a p-3">Opposte visioni</p></br>
                            <p class="p-3">
                                La discussione si apre su due opposte visioni: un'ipotesi che potremmo definire
                                hobbesiana per cui l'uomo allo stato di natura sarebbe feroce, pronto alla guerra, ad
                                aggredire i suoi simili e un'ipotesi di assoluta bontà naturale, di primigenia purezza e
                                generosità dell'uomo prima dell'istituzione della società civile. Così, se per molti
                                autori la semplice natura è sempre rozza, grossolana, ingenua o violenta, ed è quindi la
                                cultura che può sanare gli squilibri e trasformare i limiti naturali, conferendo
                                all'essere umano una sua dignità, per altri questa prospettiva è capovolta ed è invece
                                la civiltà, col suo corredo di arti ed artifici, ad essere messa sotto accusa. L’uomo
                                non ancora corrotto e indebolito dai bisogni superflui e dallo stato di squilibrio
                                psico-fisico che comportano le consuetudini e le pratiche di esistenza dettate dal
                                progresso e dalla vita in società, sarebbe mosso da una natura integra, forte, generosa.
                                Le linee generali di questa tematica vanno da Hobbes a Voltaire, nell'accezione critica
                                dello stato di natura e da Montaigne fino a Rousseau, nell'opposta valenza della bontà
                                naturale, seppur con diverse sfaccettature.
                                È chiaro che, se Hobbes, nel Leviatano, pare non avere dubbi sulla naturale ferocia
                                dell'uomo, per altri autori questa posizione è talora sfumata se non contraddittoria e,
                                comunque, sempre sottoposta a vaglio critico e a discussione, come in Diderot o nello
                                stesso Voltaire. Quest'ultimo, che pure propende per attribuire alla cultura ogni
                                possibilità di mitigazione degli istinti aggressivi della natura umana, in racconti
                                filosofici come l'Ingenuo, pare accogliere la tesi opposta, lasciando aperto il problema
                                ma senza offrire una risposta univoca.
                                In una celebre opera, il Discorso sull’origine e i fondamenti della diseguaglianza fra
                                gli uomini (1755), Rousseau pone come ipotesi di lavoro uno stato originario di natura
                                in cui si vive liberi, felici, virtuosi. Il testo è subito criticato da Voltaire che
                                accusa il suo autore di voler ricondurre l'umanità allo stato primitivo. In realtà,
                                benché Rousseau creda fermamente alla naturale bontà dell'uomo, non auspica un ritorno
                                ad un passato mitico e pastorale, ma cerca di educare l'essere umano a non soffocare in
                                sé, con sempre nuovi bisogni ed artifizi, l'istinto naturale, ed è proprio la
                                conciliazione fra natura e cultura uno dei temi di fondo che percorrono tutta la sua
                                opera. Questo dibattito rimarrà fecondo per tutto il secolo seguente, in Italia sarà il
                                Leopardi a raccoglierne l'eredità, mentre nel Novecento, lo sviluppo degli studi di
                                etnologia ed antropologia ne confermeranno la ricchezza, attraverso nuovi orizzonti
                                epistemologici.
