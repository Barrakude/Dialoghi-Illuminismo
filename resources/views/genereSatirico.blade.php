                        <h2>Il Genere Satirico</h2>
                        <div class="article-preview">
                            <p>La satira è nel Settecento un genere molto diffuso. L'ispirazione satirica, la
                                derisione e la parodia corrosiva hanno una genealogia antica: Luciano, Petronio,
                                Orazio e, tra i moderni, è soprattutto la tradizione degli autori
                                quattro-cinquecenteschi che ispira le figure satiriche. Il registro
                                eroicomico esalta le “gesta” di personaggi derisori, eretti al rango di eroi,
                                impiegando un linguaggio magniloquente, sublime, per descrivere infime imprese.
                                Rabelais (Gargantua e Pantagruele), Tassoni (La secchia rapita), Folengo, Boileau
                                (Le Lutrin), Pope (The rape of the Lock, assai noto in Italia per essere stato
                                tradotto), maestri nel genere, godono di grande successo e costituiscono le fonti
                                degli autori settecenteschi.  Romanzi e racconti che sono spesso il simulacro di un
                                racconto, e cioè parodie del romanzesco, si diffondono lungo tutto il Settecento in
                                Europa. La polemica satirica colpisce miti e pregiudizi frutto di ignoranza,
                                privilegi sociali, così come i demoni dell'oscurantismo e le superstizioni. Sul
                                piano etico sono l'irrazionalità dell'umano atteggiarsi, l'insensatezza di usi e
                                costumi, la turpe bruttura del "vizio" che divengono il bersaglio della satira nel
                                romanzo filosofico del Settecento. Sul piano sociale, gli autori satirici, pungenti
                                anatomisti della società nobiliare e borghese dell'epoca, modulano in tono ironico
                                il "conflitto di classe", mettendo allo scoperto sopraffazioni e violenze nel corpo
                                sociale. E tuttavia, proprio in questo secolo, la satira perde i lineamenti già
                                labili del genere per confondersi con l'atteggiamento critico della ragione che
                                viene opposta all'irrazionalità di istituzioni che paiono votare gli esseri umani
                                all'infelicità (LINK FELICITA’ E VIRTU’). Il discorso satirico si generalizza,
                                acquistando più ampio respiro nell'esplicitare l’amoralità, le aberrazioni,
                                l'ottusità di quei comportamenti umani che paiono concorrere più alla distruzione
                                del consorzio sociale, che al progresso della civiltà. Fenomeni storici e ideologici
                                ben precisi come la guerra, l'intolleranza politica e religiosa, il fanatismo,
                                l'ipocrisia sono spesso trattati con lo stile dell'epica degradata, come in Voltaire
                                (LINK VOLTAIRE CANDIDE LA GUERRA NEL MIGLIORE DEI MONDI POSSIBILI), o con
                                registro macabro-grottesco, come in Swift (LINK MODESTA PROPOSTA) così da farne
                                intendere l'intollerabile assurdità. Il genere satirico si avvale di alcuni
                                espedienti formali di cui fanno uso quasi tutte le opere che a tale genere si
                                richiamano, seppur con la diversità dei linguaggi e delle finalità che
                                caratterizzano i differenti autori.</p>


