                            <p class="a">Procedimenti stilistici della satira</p></br>
                            <p>
                                Le diverse retoriche sono accomunate da alcune scelte formali di fondo, tra queste
                                le più frequenti sono:</br>
                                • Il narratore fittizio. Un procedimento molto usato dai narratori del Settecento è
                                quello del "narratore fittizio", che consiste nell'attribuire il racconto ad autori
                                diversi da quello reale.</br>
                                • Voltaire si cela frequentemente sotto pseudonimi di fantasia, per esempio
                                nell'introduzione al Candido si legge: "tradotto dal tedesco"; l'autore è un certo
                                "dottor Ralph", Zadig è presentato come "il libro di un antico saggio", tradotto in
                                arabo da Sadi (*autore realmente esistito, è uno dei più celebri poeti persiani,
                                vissuto tra il 1200 e il 1291).</br>
                                Il procedimento presenta alcuni vantaggi:</br>
                                a)  evitare di essere classificato come autore di romanzi, dato che il genere è poco
                                apprezzato dalla cultura dell'epoca</br>
                                b)  fornire origini serie al romanzo o al racconto, illustrandone il peso storico
                                attestato dalla sollecitudine dei traduttori</br>
                                c)   sfuggire alla censura e poter liberamente esprimere ogni forma di critica
                                sociale e religiosa.</br>
                                • la distanziazione, lo straniamento: in virtù di tali effetti di inverosimiglianza, il
                                lettore è sempre confrontato a fatti improbabili che ne escludono l'identificazione con
                                l'eroe e qualsiasi implicazione emozionale. Si ottengono attraverso una narrazione
                                fantastica, ricca di esagerazioni, nonsense e caricature.</br>
                                • Il discorso ironico e antifrastico. Ampiamente usata ancora oggi nel linguaggio
                                comune, soprattutto orale, l’ironia è una figura retorica per la quale si dice il
                                contrario di ciò che si vuol far intendere. Il Settecento ne fa ampio uso, complicandone
                                spesso le manifestazioni con il doppio registro satirico e didascalico. Parini ad
                                esempio, nel Giorno intreccia ironia e modello didascalico: nella finzione letteraria,
                                il poeta si presenta infatti come il precettore che deve illustrare al giovin signore le
                                occupazioni della sua tediosa giornata. In un testo scritto l’ironia è particolarmente
                                problematica, tanto per la varietà degli impieghi, quanto per l’ambiguità semantica che
                                ne caratterizza sia il piano linguistico, sia quello della poetica: là dove il discorso
                                scritto manca dell’ausilio dell’intonazione, rimane difficile cogliere ciò che è detto
                                ironicamente, anche se, molto spesso, l’inverosimiglianza di certi effetti aiuta
                                sensibilmente a coglierne gli accenti ironici.</br>
                                • la parodia: imitazione burlesca, grottesca e cinica.</br>
                                • la caricatura: può essere una descrizione, o un disegno, che attraverso l'esagerazione
                                di certi tratti dà di una persona una rappresentazione satirica.</br>
                                • lo stile eroicomico, l'eroico degradato o l'epica degradata: le forme alte e sublimi
                                dei generi eroici, della letteratura cavalleresca, della letteratura epica vengono
                                interpuntate di invenzioni linguistiche ricalcate sul volgare (rumpitur in testa), le
                                forme alte, sublimi, le espressioni relative ad una tematica bellica, nobile e gloriosa
                                vengono usate per designare realtà infime, banali o ignobili.</br>
                                • la magnificazione ironica per mezzo di vari espedienti (come l'iperbole) esalta il
                                vacuo, il piccolo, ciò che è insignificante, Parini ne fa uso nel Proemio
                                del Mattino magnificando il "sangue blu" del giovin signore. Anche nelle arti plastiche
                                si può osservare la compiaciuta celebrazione del grazioso e del minuto.
                            </p>



